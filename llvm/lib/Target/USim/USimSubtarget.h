#ifndef LLVM_LIB_TARGET_USIM_USIMSUBTARGET_H
#define LLVM_LIB_TARGET_USIM_USIMSUBTARGET_H

#include "USimFrameLowering.h"
#include "USimISelLowering.h"
#include "USimInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "USimGenSubtargetInfo.inc"

namespace llvm {

class StringRef;
class TargetMachine;

class USimSubtarget : public USimGenSubtargetInfo {
  virtual void anchor();

  USimRegisterInfo RegInfo;
  USimInstrInfo InstrInfo;
  USimFrameLowering FrameLowering;
  USimTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  USimSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
                const TargetMachine &TM);

  /// Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const USimInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const USimFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const USimTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const USimRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_USIM_USIMSUBTARGET_H
